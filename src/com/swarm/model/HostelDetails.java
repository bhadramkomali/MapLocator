package com.swarm.model;

public class HostelDetails {
	private String areaName;
	private String hostelName;
	private String ownerName;
	private String hostelAddress;
	private String contactNumber;
	private double hostelFee;
	private double hostelLat;
	private double hostelLong;

	public String getAreaName() {
		return areaName;
	}

	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}

	public String getHostelName() {
		return hostelName;
	}

	public void setHostelName(String hostelName) {
		this.hostelName = hostelName;
	}

	public String getOwnerName() {
		return ownerName;
	}

	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}

	public String getHostelAddress() {
		return hostelAddress;
	}

	public void setHostelAddress(String hostelAddress) {
		this.hostelAddress = hostelAddress;
	}

	public String getContactNumber() {
		return contactNumber;
	}

	public void setContactNumber(String contactNumber) {
		this.contactNumber = contactNumber;
	}

	public double getHostelFee() {
		return hostelFee;
	}

	public void setHostelFee(double hostelFee) {
		this.hostelFee = hostelFee;
	}

	public double getHostelLat() {
		return hostelLat;
	}

	public void setHostelLat(double hostelLat) {
		this.hostelLat = hostelLat;
	}

	public double getHostelLong() {
		return hostelLong;
	}

	public void setHostelLong(double hostelLong) {
		this.hostelLong = hostelLong;
	}

}
